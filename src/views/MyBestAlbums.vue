        <div class="text-left text-h5 article_headline" id="headline1">"Midnite Vultures" - Beck (1999)</div>
        <q-img src="../assets/picturesForBlogs/midnite_vultures.jpg" class="album_img"></q-img>
        <div>
            <p style="font-size: 1rem; line-height: 180%;">
                このアルバムを知らない者でも、Beck氏のシングル『Loser』やアルバム『Odelay』などを知っている者もいるかもしれない。
                しかもBeck氏は、近年ではあのPharrell Williams氏とコラボしていたりと、今も時代の最先端を行く「イケている」アーティストである。<br>
                さて、本アルバムのカバーは蛍光色の緑とピンクがコントラストになっていて、非常に目が痛くなってくるだろう。
                アルバムの中身もこのサイケデリックさに負けぬくらいカオスなものとなっている。<br>
                <br>
                1曲目の『Sexx Laws』は軽快なトランペットからはじまり、比較的ポップな仕上がりとなっている。
                しかしながら、端々に散りばめられた機械音や歌詞の支離滅裂さは本アルバムの1曲目にふさわしいだろう。
                2曲目以降は『Nicotine & Gravy』『Mixed Bizness』...と続いていき、少し全体的な雰囲気が暗くなる。
                『Mixed Bizness』はシングルカットされており、『Sexx Laws』と同様に金管楽器・機械音・コーラスがいい意味で混ざりきっておらず、ケオティックな世界観を醸し出している。<br>
                <br>
                8曲目『Milk & Honey』はRadio Headを想起させるような重苦しいイントロから始まる。
                それ以降も当時全盛期だったオルナティブ・ロックに遊び心を加えたようなメロディーが続き、Beck氏の才能が伺える。
                10曲目『Pressure Zone』はヒューマンビートボックス、刀で何かを切る音、スプレーの音などとにかく色々な音が一つの曲を織りなしており、
                そのコラージュともパッチワークとも言えぬクリエイティブな世界観に引きずり込まれること間違いなしだ。
                この刀で何かを切る音は、Prince氏のアルバム『Love Symbol』(1992)からのシングル『7』中でも用いられており、
                時系列的にもここから着想を得たのではないかと思われる。<br>
                <br>
                トリを飾るのは『Debra』。個人的には、自分の知っている全ての音楽の中でベスト5に入る名曲である。<br>
                また、映画『<a href="https://www.sonypictures.jp/he/2011452">BABY DRIVER</a>』で主人公がこの曲を流すシーンがあるので、聴いたことがある者もいるのではないだろうか。<br>
                曲調はゆったりとしており、チェロ(コントラバス？)の奏でるベースラインはゆりかごで揺らされているような心地よさを覚える。
                この曲はこれまでの曲とは違い、Beck氏が務めるボーカルは非常に甲高い声となる。
                非常にドラマチックな歌い方であるが歌詞の内容をよく見ると、タイトルの「Debra」という女性はただの主人公のカノジョの妹という、何とも言えない脇役。
                他にも「I said, lady, step inside my Hyundai (Hyundai=韓国の自動車メーカー『現代』のこと)」などの言い回しなど、
                Beck氏がこれでもかと言わんばかりの遊び心を詰め込んできているのも非常によい。まさにトリを飾るにふさわしい曲と言える。<br>
                <br>
                90年代のオルナティブ・ロックが好きで少し変わり種を求めている者にはオススメのアルバムだ。<br>
            </p>
