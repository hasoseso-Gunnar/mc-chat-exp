        <div class="text-left text-h5 article_headline" id="headline5">"Black Tie White Noise" - David Bowie (1993)</div>
        <q-img src="../assets/picturesForBlogs/black_tie_white_noise.jpg" class="album_img"></q-img>
        <div>
            <p style="font-size: 1rem; line-height: 180%;">
                さて、本記事のトリを飾るのはDavid Bowie氏の『Black Tie White Noise』だ。<br>
                最近、David Bowie氏のドキュメンタリー映画『Moonage Daydream』が公開され筆者も映画館に足を運んだが、
                まぁ正直ちょっと期待外れだった。私レベルの熱烈なファンとなると、何回も観たライブ映像などばかりで少し退屈になってしまうかもしれない。
                一方で、David Bowieという偉人について知りたいという初心者レベルの人にとっては、非常に楽しめるものなのではないだろうか。<br>
                今回の記事のテーマは「必聴！知られざる名盤たち」だったわけだが、David Bowie氏の「知られざる名盤」はあまりにも多すぎて選ぶのが大変だった。
                とりあえず今回は聴きやすさ重視ということで、比較的ポップでキャッチーなアルバムを選定した。<br>
                本アルバムはDavid Bowie氏が再婚した直後にリリースしたアルバムで、新婚生活の明るく希望に満ちた様子がそのまま楽曲で表現されている。<br>
                <br>
                1曲目は『The Wedding』は読んで字の如くで、教会の金の音と共に始まるインスト(=歌詞やボーカルのない)曲である。
                2曲目『You've Been Around』もインストで、3曲目『I Feel Free』でやっとボーカルが現れる。
                『I Feel Free』はサックスの使い方などを含めて下地はかなりソウル寄りなのだが、それを機械音などを用いて見事にポップな曲調に変換している。<br>
                David Bowie氏はソウルやジャズなどの黒人音楽についての造詣が深く、『Young Americans』『Station to Station』などではそういった技術が遺憾なく発揮されている。
                『I Feel Free』もそういった経験の成せる業だろう。<br>
                <br>
                4曲目『Black Tie White Noise』はゲストボーカルのAl B. Sure!とのコラボ作品で、当時台頭し始めた「ラップ」というジャンルの要素を上手く取り込んでいる。
                今となってはラップを知らない人などいないだろうが、この当時から目を付けていたDavid Bowie氏の情報感度の高さには驚かされる。<br>
                5曲目『Jump They Say』はシングルカットもされるくらいにかなりポップだが、実際の歌詞は精神病を患っていた実兄をテーマにしており、ダークである。
                MVもかなり手が込んでいて、30年経った今でも古さを感じさせない出来なので、興味がある諸君には見て頂きたい。<br>
                <br>
                8曲目の『Miracle Goodnight』は個人的にはお気に入りである。曲のメロディーから感じられるそこ抜けた明るさが、
                まさにこのアルバムのコンセプトを体現していると言えるだろう。
                6曲目の『Nite Flights』はthe Walker Brothers、
                11曲目の『I Know It's Gonna Happen Someday』はThe SmithのボーカルMorrisseyの曲のカバーだが、
                他のオリジナル曲と負けず劣らない完成度である。
                どちらも原曲の良さを活かし、そこにオリジナリティを加えるというカバーソングのお手本のような形を取っている。<br>
                他にも、ボーナストラックの『Real Cool World』と『Lucy Can't Dance』もアップテンポでイケイケな感じなので、
                是非ともデラックスエディションでの鑑賞をオススメしたい。<br>
