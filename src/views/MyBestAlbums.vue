        <div class="text-left text-h5 article_headline" id="headline4">"Skylarking" - XTC (1986)</div>
        <q-img src="../assets/picturesForBlogs/skylarking.jpg" class="album_img"></q-img>
        <div>
            <p style="font-size: 1rem; line-height: 180%;">
                ガストやバーミヤンなどを経営する飲食業界の重鎮「すかいらーくグループ」だが、
                この「すかいらーく」はskylark、つまり鳥のヒバリのことである。
                また、動詞としては「バカ騒ぎをする」とか「ふざける」という意味があり、本アルバムの意味はこちらの方だと考えるのが自然であろう。<br>
                <br>
                さて、本アルバムは制作過程にいわくがついてるとかなんとかで、そちらの方が取り沙汰されることが多いそうだが、名盤であることには変わらない。<br>
                なお、先ほどから〇曲目とアルバム内での順番を記しているが、このアルバムについては少し特殊でバージョンによって順番が違うそうで、
                筆者はとりあえずApple Music上でのアルバムの順番に従っている。<br>
                <br>
                1・2曲目『Summer's Cauldron』『Grass』は連続した曲となっており、『Summer's Cauldron』は虫や鳥の声が聞こえる夜の草原にいるような感覚に陥る。
                『Grass』では夜が明け、朝の眩しい光の中で草原に寝っ転がっているような情景が浮かんでくる。<br>
                このアルバム全体、引いてはXTCの作品多くに言えることだが、このバンドは情景が浮かんでくるような音作りが非常に上手である。
                その点においては、このアルバムはEcho & the Bunnymenの『Porcupine』と非常に似通った部分があるように思える。<br>
                <br>
                4曲目『That's Really Super, Supergirl』は「Supergirl」とあるように、意中の相手を女性版スーパーマン、
                つまり「Supergirl」と喩えているのである。この曲は弾けるような、まさにポップさが最大の聴きどころで非常にキャッチーである。
                5・6曲目の『Ballet For A Rainy Day』『1000 Umbrellas』も連続した一つの曲となっている。
                個人的にはこの曲には聴いている人への「裏切り」が多く含まれているように感じる。
                諸君らも初めての曲を聴いている時に、「流れ的に次はサビだな」等となんとなく感じながら音楽を訊くかもしれないが、これら2つの曲はそういった予想を裏切ってくるように感じる。
                転調に次ぐ転調があったり、「これは繰り返さないだろう」という部分で歌詞を繰り返したり、「あ、ここでこの曲終わるのかな」というところで続いたりと、
                これが意図的なのかどうか分からないが、結果的に非常に面白い曲の構成を生み出していると言えよう。<br>
                <br>
                9曲目は『Big Day』。タイトルの通り「今日はこれからデカいことをやるぞ」と意気込んでいる人の鼻息が伝わってくるような曲となっている。
                大金をポケットに詰め込み、大勝負をしに競馬場に入っていくような、そんな情景を思い浮かべる不思議な音楽だ。<br>
                そして11曲目の『Mermaid Smiled』。この曲は元々ボーナストラックだったそうだが、リマスターされてアルバムの一部となっているようだ。
                タイトルに「Mermaid」とあるので、当然ここは雄大な海を思い浮かべるべきなのだろうが、
                自分はあたり一面に広がる雪原で、枯れた一本の大木を目印に進んでいるような、そんな情景がまぶたの裏に映し出されるのだ。<br>
                <br>
                14曲目の『Sacrificial Bonfire』は、このアルバムの中でも1,2番目に好きな曲だ。
                力強いボーカルと(チェロかコントラバスか分からないが)重低音の弦楽器が波のように揺れるベースラインをリードする。
                サビではドラムが合流し、2Aメロからは高いバイオリン(かヴィオラ)が加わり、メロディーラインが豪華になる。
                そのような調子で徐々に様々な楽器がメロディーに加わっていき、最後の大団円を迎える、
                と思わせてラスサビ後に途中から加わった楽器たちは離脱し、最初のボーカルと弦だけの状況に戻る。<br>
                『Sacrificial Bonfire』というタイトルもあいまって、輪廻転生や諸行無常といった言葉を想起させる、非常に面白い曲である。<br>
                <br>
                今回紹介するアルバムの中でも割と取っ付きやすいと思うので、特に好きなジャンルが無いという者にオススメだ。<br>
            </p>
