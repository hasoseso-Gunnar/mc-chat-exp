        <div class="text-left text-h5 article_headline" id="headline2">"Rainbow Children" - Prince (2001)</div>
        <q-img src="../assets/picturesForBlogs/rainbow_children.jpg" class="album_img"></q-img>
        <div>
            <p style="font-size: 1rem; line-height: 180%;">
                さて、先ほどもPrince氏の名前が度々挙がったが、かくいう私もPrinceの大ファンである。
                日本では知名度の低いPrince氏だが、80年代のアメリカではかのMichael Jackson氏と「King of Pop」の座を争っていた実はスゴイ人なのだ。<br>
                本アルバムは、Prince氏がエホバの証人に入信した時期(厳密には2003年入信)と被っており、当時の彼の宗教観や政治観を強く反映させたアルバムとなっている。
                また、本アルバムはコンセプト・アルバムとなっており、アルバム全体が一つの物語となっている点もアルバムとしての完成度を上げている。<br>
                1曲目にしてタイトル・トラックの『Rainbow Children』はボイチェンしたプリンスの低いナレーションからジャジーな曲調へと切り替わる、
                実験的なオープニング。最初のリズム自体は軽快ながらも、次第に盛り上がりを見せていき、曲のメロディーがどんどん積み上がって行く過程を楽しめる。
                続いて2曲目の『Muse 2 The Pharaoh』はゆっくり丁寧に進行していくボーカルとメロディーによって、真空にいるような澄んだ気持ちになるファンク。
                5曲目の『Everywhere』はそれまでと打って変わり、スローテンポとアップテンポを繰り返す若干ポップな曲となっている。<br>
                <br>
                後半、途中『1+1+1 Is 3』などでは変則的・前衛的な曲も入ってくるものの、
                11曲目の『She Loves Me 4 Me』では2曲目と同じようなスローテンポで綺麗なファンクへと回帰する。<br>
                <br>
                そして、最後は8分の長尺の曲が3つ続くという、これまた少し変則的な順番となっている。
                長尺三連続、最初を務めるのが『Family Name』。
                コイツはちと問題児で、かの有名な「タモリ俱楽部」の「空耳アワー」のコーナーで3回も登場しているかなりレアな曲である。
                しかもそのうちの1回「おーい、鬼太郎、働け！」という空耳は最高賞の空耳アワージャケットを受賞しているのだ。
                まぁ、それはさておき曲の歌詞自体はかなり政治的で、当時は賛否両論あったそうだ。
                というか、人種問題に突っ込んだなかなか攻めた曲であることには間違いない。<br>
                <br>
                それから、続くは『The Everlasting Now』。ライブっぽい環境音を織り交ぜつつ、前曲とは変わってかなりポップな仕上がり。
                アルバム最後の曲『Last December』は<a href="https://npg-net.com/discography/albums/the-rainbow-children-01/">こちら</a>のサイトでも紹介されているように、
                『The Gold Experience』の最後の曲『Gold』や有名な『Purple Rain』のようなグルーヴィーで、ドラマチックなフィナーレとなっている。<br>
                <br>
                ちなみに今紹介した『The Gold Experience』はジョジョ5部主人公ジョルノのスタンドの元ネタとなったアルバムで、こちらもオススメである。
                今回紹介するPrince枠のアルバムで、個人的には『Rainbow Children』と『The Gold Experience』の間でかなり迷ったが、
                最終的にはアルバム全体としての完成度を評価しこちらを取り上げることにした。<br>
                <br>
                ソウルやファンクなどのジャンルが好きな諸君には是非ともオススメしたいアルバムだ。
            </p>
