        <div class="text-left text-h5 article_headline" id="headline3">"HARD TO BEAT" - The Beat (2017)</div>
        <q-img src="../assets/picturesForBlogs/hard_to_beat.jpg" class="album_img"></q-img>
        <div>
            <p style="font-size: 1rem; line-height: 180%;">
                今回紹介するアルバムは、基本的にはスタジオアルバムから選定したかったが、これのみ例外的にベストアルバムである。<br>
                というのも、本アルバムを制作したバンドであるThe Beatは3つのスタジオアルバムしか作成しておらず、
                その中でも『I Just Can't Stop』(1980)と『Special Beat Service』(1982)で収録された曲が本アルバムに集約されている。
                そのため、ベストアルバムと言ってもほぼ二つのアルバムを合わせただけなので、ご容赦願いたい。<br>
                まぁ、そんな御託はさておき、個人的にThe Beatというバンドは最も過小評価されているバンドだと思っている。
                その理由は、このアルバムを聴けば分かることだろう。<br>
                <br>
                1曲目は『Mirror in The Bathroom』。素早いスネアから始まる、印象的な曲だ。
                こちらの曲はサックスとベースのメロディーのよって奏でられる、レゲエともジャズともポップとも言えない曲調が少し怪しげな雰囲気を演出しているように感じる。
                The Beatの曲はレゲエなどの黒人音楽に振り切っている場合と、ポップ系の白人音楽に振り切っている場合が多いが、
                この曲は上手い具合にその二つが融合している。<br>
                2曲目『The Tears of a Clown』は、明るいレゲエ調の音楽とは反し歌詞の内容は若干重め。
                明るく振舞おうとすることで悲しみを隠そうとする歌詞の内容とマッチしているのも興味深い。<br>
                5曲目『Hand Off... She's Mine』は映画『50回目のファーストキス(原題: 50 First Dates)』の挿入歌でもあるので、聴いて思い出す人もいるかもしれない。
                映画の舞台は確かハワイかグアムであると記憶しているが、南国感漂う本曲が採用されるのも不思議ではない。
                特に木琴のメロディーなどは『リトルマーメイド』のサウンドトラックさながらである。<br>
                <br>
                『Too Nice to Talk to』『Best Friend』などはレゲエやジャジーな曲調から脱却し、かなりポップな感じとなっている。
                かたやゴリゴリのレゲエもある中、これらの曲などは雰囲気をぶち壊す訳でもなく上手い具合に調和している。<br>
                特に私が本アルバムの中で好きなのがこれら2つの曲の前後にある『Sole Salvation』と『I Confess』だ。
                <br>
                『Sole Salvation』は、ベースによるアップテンポのスタート、そこに力強いサックスが合流し、
                最後にまくし立てるようなボーカルによっていよいよ歌詞パートが始まるといったイントロになっている。
                この一定のリズムを刻み付けるベースラインと、途中のソロも含めた演奏力の高いサックス、
                そして淡々と事実を語るようないい意味で「歌っていない」ボーカルのマリアージュが何とも言えない魅力を感じさせる。<br>
                一方、『I Confess』はピアノがメロディーラインを担っており、逆にボーカルはかなり「歌っている」。
                ピアノ、ドラム、コンガ、ベースくらいしか常に鳴り続けている楽器はないのだが、
                ピアノとボーカルの存在によって驚くほど音に深みが生まれている。
                曲の構成も素晴らしく、トランペットのソロパートなども耳の心地が良い。
                MVもシンプルだが、クリエイティブで非常に好きである。<br>
                <br>
                The Beatは曲の完成度に対して知名度が低いが、この機会に是非ともレゲエ好きの諸君には聴いてもらいたい。<br>
            </p>
