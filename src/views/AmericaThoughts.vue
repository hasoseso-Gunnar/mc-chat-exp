                そんな時、日本であればそこそこのホテルであればウォシュレットが付いているのは当たり前だが、アメリカを含めた欧米圏にはウォシュレットというものが存在しない。
                むしろ日本のウォシュレット文化は奇怪なものとして海外の映画や小説で描かれることすらある。<br>
