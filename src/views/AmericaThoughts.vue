                <br>
                最後に、アメリカの「お土産」の少なさについてもここに記しておきたい。<br>
                日本人であれば、旅行に行った際に家族や職場やその他のコミュニティのためにお土産を買って帰ることも少なくないだろう。
                それ自体はいいのだが、アメリカでお土産を探す際には注意してもらいたい。
                どういうことかと言うと、日本人が考えるような「博多通りもん」や「もみじ饅頭」のような箱に個包装された名物のお菓子が入っている「お土産」が売られている場所は非常に限定的である。
                私は空港の売店で典型的な「お土産」を購入したが、品揃えは非常に悪く値段も高かった。<br>
                それもこれも、海外には日本のような「お土産」文化がないことが原因だろう。
                とにかく、私は運よく見つけることができたが、アメリカ渡航の際には(特に観光地でない場所だと)このような典型的な「お土産」を購入できるかは怪しいので、
                事前のリサーチが肝要である。
                一方で、その土地の名所が描かれたマグカップや観光名所のミニチュアなど、自分用の「お土産」のバラエティーは結構豊富なので、
                そこに関しては安心してもらいたい。
