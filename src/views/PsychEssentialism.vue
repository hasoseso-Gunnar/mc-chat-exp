                <br>
                週刊少年ジャンプの連載作品において、1980～1990年代は主人公が特別な「血統」であるというパターンが多かったが、
                2000年代以降からは「偶然」能力を会得するシナリオが急増する、というのが私の主張だ。<br>
                <br>
                では、なぜこういった「流行り」は生まれたのだろうか。私の考える答えはシンプルで、
                それぞれお互いの時代に「なかった」ものがフィクションとして好まれる傾向にあったのではないかと推測する。<br>
                もう少し具体的に説明しよう。<br>
                <br>
                1980～1990年代の初めの日本と言えば、何を思い浮かべるだろうか。好景気、土地神話、一攫千金、ザギンでシースー...。
                そんな今では夢みたいな言葉が人々の口から日常的に発されていた。(銀座で寿司くらいは、今でもランチでなら諸君らも食えるかもしれない)<br>
                そして、その頃は今よりもずっと多くの人にとってチャンスが身近なものであったと考えられる。
                つまり、わざわざフィクションの中にまでそんなもの求めなくたってよかったわけだ。<br>
                そんな時代だから、漫画も空条承太郎やケンシロウのような勢いがあって大味で、めちゃくちゃ強いキャラクターが受け入れられのだろう。
                彼らが強い理由も、心理的本質主義的に理解できる程度の設定でよかったのだ。<br>
                また、そもそも論として、まだこの時代の読者は目が肥えていなかったというのも大きいだろう。
                1960～70年代にトキワ荘の面々によって現代に通ずる漫画の表現技法は確立され、
                1980年代はまだ大友克洋氏や鳥山明氏による画力面でのブレイクスルーが進められている時代であった。<br>
                この頃は、まだまだ発展途上であった漫画というジャンルにおいて、物語の設定ごときにあーだこーだ言う人は少なかったのかもしれない。<br>
                <br>
                逆に2000年代と言えば、まあ、この流れでは諸君らはあまり良さそうなイメージは抱かないだろう...。<br>
                不景気、停滞、少子化、回転寿司で迷惑行為...。あまり社会派を気取るつもりはないのでこの辺でやめておくが、
                まあざっと今の世の中はこんなところだろう。<br>
                「親ガチャ」などと揶揄されるように、現代の日本は門地の違いによる経済格差や教育格差などが残念ながら存在する。
                当然、こんなものは昔からあったのだが、たぶん昔に比べて逆転できるようなチャンスが減ってしまったことが、人々の不安の原因だろう。<br>
                こんな時に、私たちは少年ジャンプの連載作品にまで「親ガチャ」で力関係が決まるストーリーを見たいだろうか。
                いや、むしろせめてフィクションの中だけでもいいから、「偶然」によってチャンスが得られる、そんな主人公を見たいだろう。<br>
                その現代の情勢を踏まえた「必然」の結果によって、「偶然」力を得る主人公が好まれる傾向へと変わって行ったのではないかと考察する。<br>
                また、先ほどとは逆に、読者の目は確実に肥えてしまった。本でも映画でも音楽でもそうだが、新しい時代に新しい発明はつきものだ。
                マンネリ化した設定から脱却するために、「心理的本質主義設定」の脱却が行われたという側面も少なからずあるだろう。<br>
                <br>
