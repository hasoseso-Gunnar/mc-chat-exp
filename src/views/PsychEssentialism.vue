                    以上が、私の持つ非常に狭い知見と多少の偏見も混じった考察である。
                    もし、我こそは反論したいということであれば、下のコメント欄から批判的かつ建設的な意見を述べてほしい。
                    <br>
                    今回の法則だが、案外世界を見渡してみると、海外の映画作品などにも適用できるのではないかという気がする。<br>
