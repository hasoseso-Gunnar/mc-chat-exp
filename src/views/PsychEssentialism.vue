                    なんとなく察してきたのではないだろうか。<br>
                    冒頭でも述べたように、少年ジャンプのテーマは「友情・努力・勝利」だ。特に重要なのは3つ目の「勝利」だろう。
                    毎度毎度敵に負けてばかりの主人公では、読者に呆れられてしまう。読者が喜ぶのは、やはり自分が応援している主人公サイドが勝ったときだろう。<br>
                    まぁ、ここまではよいだろう。<br>
                    <br>
                    問題は、<strong>勝つためには「勝った理由」が必要</strong>という点だ。
                    多くの場合、ジャンプ漫画には特有のパワーシステム(波紋・念能力・呪術・その他多様な武術など...)が存在する。
                    主人公も何らかの能力を身に付けて敵と戦う、というのが少年ジャンプの王道バトル漫画における定番だ。<br>
                    では、どうやってその能力を身に付けたのか、この過程というのは読者にとっても非常に興味がそそられる部分だ。<br>
                    <br>
                    ここで登場するのは他でもない「本質」だ。<br>
                    先ほど登場した週刊少年ジャンプの3つのテーマのうちの残り二つ、「友情」と「努力」は本質的な説明に用いることができるだろう。<br>
                    「めちゃくちゃ努力したから能力が開花して、仲間と助け合ったから勝てたんだよ」というのは悪くない説明のような気もする。
                    特に「友情」と「努力」を体現する作品と言えば、80年代であれば『ジョジョの奇妙な冒険 第二部』や近年であれば『僕のヒーローアカデミア』などではないだろうか。<br>
                    <br>
                    ただ、いま挙げた2つの作品、そしてそれ以外の多くの作品において「能力を身に付ける」タイミングというのは必ずしも「努力」によってではないことを、
                    みなさんはすでにご存じだろう。<br>
                    では、能力を身に付けるまでのシナリオ、同時になぜ主人公は主人公なのか=なぜ主人公は「特別」なのかを説明する要因として、私は大きく2つあると考えた。
                    また、それらは特に80～90年代によく用いられた要因と、00年代以降によく用いられる要因の2パターンあると考えた。<br>
                    それらについて、順を追って説明していきたい。<br>
                    <br>
                </p>
