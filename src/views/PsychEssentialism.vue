                <a href="https://psych.or.jp/publication/world090/pw06/">こちら</a>のサイトから引用した方が分かりやすいだろう。
            </p>
            <blockquote>
                <p>ある人が「その人」であるのは，「その人」が生まれながらに持っている本質のせいだという考え方を心理的本質主義と呼ぶ。
                    特に，社会的カテゴリーの認知において，この心理的本質主義の考え方は適用されやすい。
                    例えば，「女性は遺伝的に家事に向いている」とか，
                    「あの人は外国育ちだけれど，日本人の血を引いているから奥ゆかしい」などといった言及がみられるなら，
                    その発言者は「女性」や「日本人」といった社会的カテゴリーの認知に心理的本質主義を適用しているということになる。
                </p>
            </blockquote>
            <blockquote>
                <p>ここまで，心理的本質主義の特徴について述べたが，本質の実体は具体的に定義できなくてもよいとされている。
                    「本質」は自然で，生得的で，分類可能で，表面化していない「何か」であれば，実は何であってもよいのである（Medin & Ortony, 1989）。
                    日常場面では，本質の「仮の姿」としてDNAや血などの人の資質が比喩的に用いられる。
                </p>
            </blockquote>
            <p style="font-size: 1rem; line-height: 180%;">
                非常に分かりやすい説明だ。まだピンと来ていない人にもう少し説明すると、
                例えば、両親が学校教師の家庭の子供が実際に学校教師になっていたら、「なるほど、この子は両親の血を引いたんだな」などと素朴に思うことがあると思う。
                この場合、子供が学校教師になったのは両親の「血統」に「本質」があると考えていることになる。<br>
                こういった思考の傾向が心理的本質主義と呼ばれており、もちろん、こういった風に考える傾向が強い人もいれば、そうでない人もいる。
                なんにせよ、用語自体は小難しいが、概念自体は理解しやすいものではないだろうか。<br>
                <br>
            </p>  
