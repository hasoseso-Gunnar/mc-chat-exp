                先ほどは、第二部が「友情」と「努力」による「勝利」であることを紹介したが、
                主人公ジョセフ・ジョースターはラスボスに挑むにあたって「努力」と呼ばれるようなものは1ヶ月しかしていないことが作中で説明されている。<br>
                そもそも1ヶ月の修行で済んだのも、ジョセフが祖父ジョナサンの<strong>「血統」</strong>を受け継いでいて才能があったからである。<br>
                第三部主人公の空条承太郎に至っては「ジョースター家の血を引き継いでいる」
                という以外の明確な理由もなく作中最強の「幽波紋(スタンド)」を会得している。<br>
                <br>
