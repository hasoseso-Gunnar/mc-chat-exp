                主人公孫悟空は、諸君らもご存知の通りサイヤ人という宇宙人だ。熱烈なファンであれば「悟空がサイヤ人だと明かされたのは、かなり後だ」
                とツッコミを入れたくなるかもしれないが、一旦矛を収めて欲しい。<br>
                確かに、物語の序盤において孫悟空はサイヤ人だという説明はされていなかったが、尻尾が生えていて、
                満月の夜に大猿になって暴れている。これは明らかにブルマやヤムチャ、そしてその他の「人間」とは特徴が異なる。<br>
                当時の読者は心のどこかで、孫悟空が人間離れした強さであるのは<strong>「人間ではない」</strong>からだと分かっていたはずだ。
                (もしくは、人間ではないと明言されていたかもしれないが、読んだのが昔過ぎて忘れてしまった...。)<br>
