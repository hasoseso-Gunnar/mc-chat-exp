        <div>
            <p style="font-size: 1rem; line-height: 180%;">
                <br>
                先ほどまでは2000年代以前のジャンプ作品の主人公が、
                心理的本質主義的な考え方によって特別な「能力」や「才能」を持ち併せていることを論じた。
                では、2000年代ではどういった主人公が多いと言えるのか。それはズバリ「偶然」主人公になったというケースだろう。<br>
                例を挙げて論じていく。<br>
                <br>
                『僕のヒーローアカデミア』の主人公の緑谷出久はその代表例だろう。<br>
                彼は「個性」と呼ばれる超能力を持つ人々が生活する世界において、少数派の何も能力を持たない「無個性」として生まれてきた。
                ある日、<strong>「偶然」</strong>最強のヒーローであるオールマイトから「ワン・フォー・オール」という特殊な個性を引き継ぐことになる...。
                知っている諸君も多いと思うが、シナリオはざっとこんな感じだ。<br>
                私は、最新刊まで漫画を追っていないのでもしかするとすでに「偶然」が「必然」であった理由が明かされているかもしれないが、
                少なくとも序盤はこういった形で物語が進んでいく。<br>
                <br>
                もう一つ例を挙げよう。現在第二部が好評連載中の『チェンソーマン』だ。<br>
                『チェンソーマン』の世界は、悪魔と呼ばれる魑魅魍魎が跋扈する1990年代の日本を舞台としている。
                主人公デンジはある時、<strong>「偶然」</strong>出会ったチェンソーの悪魔と契約を交わし、
                紆余曲折あってチェンソーの悪魔を内に宿す「人間でも悪魔でもない存在」となり、最終的に悪魔を駆逐するために公安へと入る...。<br>
                こちらも粗筋はこんなところだろう。
                第一部終盤にて、チェンソーの悪魔には特別な能力があることが明らかになるが、
                私の知る限りでは主人公デンジはあくまで普通、というよりド底辺を生きるただの可哀想な人間だ。<br>
                <br>
                一方で、2000年代に入ってからは、物語序盤で<strong>「偶然」</strong>能力を手に入れたが、後から重要な血縁関係が明かされる、
                というパターンも多く存在するように思う。<br>
                『ワンピース』の主人公モンキー・D・ルフィは<strong>「偶然」</strong>悪魔の実と呼ばれる特殊な果実を食べてしまい、
                ゴム人間とかいうふざけた体になってしまうのだが、
                彼の父親やひいては一族が『ワンピース』の世界における重要な人物たちであることが物語の中盤以降で明らかになる。<br>
                <br>
                同じようなパターンは『BLEACH』や『呪術廻戦』の主人公でも見られる。<br>
                『BLEACH』の主人公である黒崎一護は<strong>「偶然」</strong>死神(代行)になるのだが、彼の両親の秘密が後に明かされる。<br>
                『呪術廻戦』の主人公、虎杖悠仁も<strong>「偶然」</strong>両面宿儺という呪霊の指を食べ、
                特殊な体質ゆえに両面宿儺を取り込むことに成功するが、物語中盤にその原因は自身の親にあることが示唆される。<br>
                <br>
                いずれにせよ、『ワンピース』などの例は、掴みは「偶然」、後に「必然」であったことが明かされるといったパターンで、
                序盤の時点では、当時の読者は「珍しいこともあるもんだなぁ」と思いながら読み進めていただろう。<br>
                私は『NARUTO』は未読なので何とも言えないのだが、2000年代以降に大ヒットしたジャンプ作品を列挙してみると
                「偶然の法則」が結構当てはまるような気がする。
                逆に主人公が努力一筋で成りあがる作品というのはパッと思いつかない。(私の脳が記憶を改竄している可能性があるので、
                反証事例があれば教えてほしい。)<br>
                これをもってして、
                「心理的本質主義設定」の主人公からの脱却が図られたと結論付けるのは不自然ではないはずだ。<br>
                <br>
